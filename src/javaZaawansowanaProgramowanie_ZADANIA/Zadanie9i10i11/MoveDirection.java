package javaZaawansowanaProgramowanie_ZADANIA.Zadanie9i10i11;

public class MoveDirection {

    private double x1;
    private double y1;

    public MoveDirection(double x, double y) {
        this.x1 = x;
        this.y1 = y;
    }

    public double getX() {
        return x1;
    }

    public void setX(double x) {
        this.x1 = x;
    }

    public double getY() {
        return y1;
    }

    public void setY(double y) {
        this.y1 = y;
    }
}
